#include "librlx.h"

t_vec3	rlx_vec3_sub(t_vec3 u, t_vec3 v)
{
	return ((t_vec3){
		.x = u.x - v.x,
		.y = u.y - v.y,
		.z = u.z - v.z,
	});
}
